public class NodeHorizontalDistPair {
  Node node;
  int horizontalDistance;

  public NodeHorizontalDistPair(Node node, int horizontalDistance) {
    this.node = node;
    this.horizontalDistance = horizontalDistance;
  }

  @Override
  public String toString() {
    return "NodeHorizontalDistPair{" + "node=" + node + ", horizontalDistance=" + horizontalDistance + '}';
  }
}
